        <p class="mt-3 text-xl font-semibold text-green-600">脳の健康を守る、銀杏葉</p>
        <p class="mt-2">
            銀杏葉エキスははやくからとっていれば、あなたの記憶力の低下や思考力の<br>
            減退を防いでくれる最も強力な脳の薬です。<br>
            <br>
            身体に起こる自体が、手遅れになる前に摂りたい。記憶力の低下や、その他の<br>
            差し迫った精神崩壊の兆しが加齢と共に表れると、ほとんどの人々は無力感に<br>
            襲われ悲惨な人生を絶望的に、過ごすことになります。<br>
            <br>
            この記憶力と精神能力の減退は、医学的には認知症と呼ばれていますが、<br>
            通常避けがたい加齢の結果とされています。しかし、本当にそうなのでしょうか？<br>
            <br>
            ところがこのなんでもない銀杏(イチョウ)の葉のなかにはこの精神能力の衰えを<br>
            治療する物質が存在しており、イチョウの若葉に含まれる物質が血流を促進し、<br>
            脳の機能を高めるための優れた治療薬になっていることが確認されました。<br>
            <br>
            血管が細くなり脳細胞への血流が悪化し、記憶障害やその他のいわゆるぼけと<br>
            呼ばれる症状の一般的な原因と血管性の病気と神経変性による病気の両方に、<br>
            このハーブが高い効果を発揮することは、すでに証明されています。<br>
            <br>
            銀杏エキスとは、銀杏の若木の抽出物で、主体となる活性物質はギンコライド<br>
            と呼ばれ、ギンコライドは銀杏の若木の葉や根にしか存在せず、この自然界で<br>
            銀杏以外の植物からは発見されていないのです。<br>
            <br>
            銀杏葉エキスにはギンコライド以外にも多種類のフラボノイドが含まれており、<br>
            そのいくつかの成分比率は銀杏にしか見られない独特の特徴を持っています。<br>
            <br>
            専門家の多くは、銀杏はエキスは全ての植物性治療薬の中で最も重要なもの<br>
            と言われ1950年代から今日までに400を超える研究論文が、医学雑誌に<br>
            発表されています。<br>
            <br>
            比較対象を取った臨床試験例は50以上も行われており、記憶力の減退<br>
            集中の困難、放心、錯乱、思考エネルギーの欠如、疲労、抑鬱、眩暈、耳鳴り<br>
            などに対する治療効果が確認されています。<br>
            <br>
            また、アルツハイマー病を含む認知症治療に銀杏を用いた結果の成績も多数の<br>
            研究者から報告されており、脳血行障害に対する銀杏葉エキスの効果は、非常に<br>
            大きく脳の機能を蘇生させ、脳循環不全による様々な症状を停止させたり、逆転<br>
            させたりしていることが確認されています。<br>
            <br>
            さらに、ドイツで行われたひとつの研究では銀杏葉エキスを摂取した1時間後<br>
            には被験者の毛細血管の血流が57％も増加していることが判明されました。<br>
            <br>
            またよく管理された研究結果では、脳血行障害を2年以上患っている99名の高齢<br>
            の患者が3ヶ月銀杏葉エキスを摂取し続けた結果、71％の人々が症状が改善された<br>
            と報告されています。<br>
            <br>
            私たちが、体内に本来持っている、自然治癒力である各種の免疫防御機構と<br>
            動植物に存在する有効成分などの活用は、「心と身体の健康医学・病気は自分で<br>
            治しなさい」のテーマに沿った、最も意義のある考えであると言えるでしょう。
        </p>
